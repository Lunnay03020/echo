#include "../includes/echo.h"

void	ft_putstr(char *c, int k)
{
	int		i;
	char	re;

	i = 0;
	while (c[i] != '\0')
	{
		if (k == 4 || k % 11 == 0 || k % 16 == 0 || k % 23 == 0)
		{
			if ((c[i] == '\\') && (c[i + 1] != 32))
			{
				re = '\\' + c[i + 1];
				ft_putchar(re);
				i = i + 2;
			}
		}
		else if (k == 1 || k % 8 == 0 || k % 13 == 0 || k % 20 == 0)
		{
			if ((c[i] == '\\') && (c[i + 1] == '\\'))
				ft_putchar('\\');
			else if ((c[i] == '\\') && (c[i + 1] != 32))
			{
				ft_putchar('\\');
				ft_putchar(c[i + 1]);
				i = i + 2;
			}
		}
		ft_putchar(c[i++]);
	}
}
